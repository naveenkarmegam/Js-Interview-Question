// JavaScript is a dynamically typed language. This means that the data type of a variable is determined at runtime, 
// and the type of a variable can change during the execution of a program. 

// Dynamically typed
let variable1 = 42;  // variable1 is a number
console.log(typeof variable1);  // "number"

variable1 = "Hello, World!";  // variable1 is now a string
console.log(typeof variable1);  // "string"

// Another example
let dynamicVariable;  // undefined, but the type is not explicitly defined

dynamicVariable = 10;  // dynamicVariable is a number
console.log(typeof dynamicVariable);  // "number"

dynamicVariable = true;  // dynamicVariable is now a boolean
console.log(typeof dynamicVariable);  // "boolean"


// statically typed languages
//In contrast, statically typed languages require variable types to be declared explicitly at compile-time,
